#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    // Complete the code.
    //declare variables
    int i;
    long l;
    char ch;
    float f;
    double d;

    // scan input
    scanf("%d %ld %c %f %lf", &i, &l, &ch, &f, &d);

    //print output
    printf("%d \n", i);
    printf("%ld \n", l);
    printf("%c \n", ch);
    printf("%f \n", f);
    printf("%lf \n", d);
    return 0;
}
